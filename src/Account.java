public class Account {

    private String id;
    private String name;
    private int balance;

    public Account () {
        id = "Leeds";
        name = "Alexander";
        balance = 0;
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public int getBalance() {
        return balance;
    }
}
